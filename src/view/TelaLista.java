    private void btalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btalterarActionPerformed
            int linha = tbAlunos.getSelectedRow();
            
            if(linha < 0) {
                JOptionPane.showMessageDialog(null,"Selecione um aluno!");
            } else {
                Object o = tbAlunos.getValueAt(linha,0);
                AlunoDao cd = new AlunoDao();
                Aluno a = cd.listar("id",(int) o).get(0);
                TeladeCadastro tlc = new TeladeCadastro(a);
                tlc.setVisible(true);
                
                preencherTabela();
                
                
            }
    }//GEN-LAST:event_btalterarActionPerformed

    private void btapagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btapagarActionPerformed
        int linha = tbAlunos.getSelectedRow();
        
        if (linha < 0){
            JOptionPane.showMessageDialog(rootPane,"Selecione um aluno!");
        } else {
            Object o = tbAlunos.getValueAt(linha,0);
            AlunoDao cd = new AlunoDao();
            Aluno a = cd.listar("id",(int)o).get(0);
            
           if(JOptionPane.showConfirmDialog(rootPane, "Deseja excluir?","Excluir",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
               JOptionPane.showMessageDialog(null, "Aluno excluÃ­do com sucesso!");
               cd.excluir(a);
           } else {
            }
        }        
        
    }//GEN-LAST:event_btapagarActionPerformed

    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        if(ctpesquisa.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Digite algo no campo");
        }else{
            
            AlunoDao ad = new AlunoDao();
            Alunotablemodelo atm = new Alunotablemodelo(ad.listar("nome",ctpesquisa.getText()));
            tbAlunos.setModel(atm);
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

